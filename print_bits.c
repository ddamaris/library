void	print_bits(long double num)
{
	int			i;
	__uint128_t	n;

	i = 79;
	n = *((__uint128_t*)(&num));
	while (i >= 0)
	{
		if ((n & ((__uint128_t)1 << i)) != 0)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		if (i == 79 || i == 64)
			write(1, " | ", 3);
		i -= 1;
	}
}


